package org.jnnit.assignments_2;
//write a program to print even integers between 2 to 100
public class EvenNumbers_2 {

	public static void main(String[] args) {
		System.out.println("Even Numbers between 2 and 100");
		System.out.println("Changed to test git functionality");
		for(int i=1;i<=100;i++){
			if(i%2==0){
				System.out.println(i);
			}else{
				continue;
			}
		}

	}

}
